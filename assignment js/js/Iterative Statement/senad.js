let fib = 17
let fid = 0
let z = 0
let v = 1
console.log(z)
console.log(v)
while (fid < fib) {
    i = z + v
    console.log(i, "bfc")
    z = v
    console.log(z, "z")
    v = i
    console.log(v, "v")

    fid += 1
    console.log(fid)

} 